/** 获取所有模板的列表 */
export function listRoomTemplates() {
  return Object.values(TEMPLATES);
}

/** 按 id 取模板 */
export function getRoomTemplate(id) {
  return TEMPLATES[id] || null;
}

/** 按 tag 过滤模板 */
export function findRoomsByTag(tag) {
  return Object.values(TEMPLATES).filter((r) => r.tags && r.tags.includes(tag));
}

/**
 * 把模板的"瓦片墙体多边形"展开成场景渲染需要的瓦片格集合（"x,y" Set）
 * 用于程序化生成器接管寻路、装饰回避等流程
 */
export function expandWallTiles(template) {
  const set = new Set();
  if (!template || !template.walls) return set;
  for (const r of template.walls) {
    for (let yy = r.y; yy < r.y + (r.h || 1); yy++) {
      for (let xx = r.x; xx < r.x + (r.w || 1); xx++) {
        set.add(`${xx},${yy}`);
      }
    }
  }
  if (template.obstacles) {
    for (const r of template.obstacles) {
      for (let yy = r.y; yy < r.y + (r.h || 1); yy++) {
        for (let xx = r.x; xx < r.x + (r.w || 1); xx++) {
          set.add(`${xx},${yy}`);
        }
      }
    }
  }
  return set;
}

